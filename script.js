function searchWord() {
    const input = document.getElementById("searchInput").value.toLowerCase();
    const result = dictionary[input];

    if (result) {
        document.getElementById("word").innerText = input.charAt(0).toUpperCase() + input.slice(1);
        document.getElementById("definition").innerText = "Definition: " + result.definition;
        document.getElementById("synonyms").innerText = "Synonyms: " + result.synonyms.join(", ");
        document.getElementById("context").innerText = "Context: " + result.context;
    } else {
        document.getElementById("word").innerText = "Word not found.";
        document.getElementById("definition").innerText = "";
        document.getElementById("synonyms").innerText = "";
        document.getElementById("context").innerText = "";
    }
}
